<script>

import {mapActions, mapWritableState} from 'pinia'
import {useCustomStore} from '../stores/custom'
  
export default {
    data() {
        return {
            email: "",
            password: ""
        };
    },
    methods: {
        ...mapActions(useCustomStore, ["loginHandler", "handleCredentialResponse"]),
        login() {
            this.loginHandler({
                email: this.email,
                password: this.password
            });
        },
        handleCredentialResponseGoogle(response) {
            this.handleCredentialResponse(response);
        }
    },
    mounted() {
        const cb = this.handleCredentialResponseGoogle;
        google.accounts.id.initialize({
            client_id: "42731081542-31t6ib7ts68okuj07g9vt32geh9cgf8a.apps.googleusercontent.com",
            callback: cb
        });
        google.accounts.id.renderButton(document.getElementById("buttonDiv"), { theme: "outline", size: "large" });
    }
}

</script>

<template>
<section class="vh-100 gradient-custom">
        <div class="container py-5 h-100">
          <div class="row d-flex justify-content-center align-items-center h-100">
            <div class="col-12 col-md-8 col-lg-6 col-xl-5">
              <div class="card bg-dark text-white" style="border-radius: 1rem;">
                <div class="card-body p-5 text-center">
                  <div class="mb-md-5 mt-md-4 pb-5">
                    <div class="d-flex justify-content-center text-center mt-4 pt-1">
                      <a class="text-white"><img src="../assets/logoOrtic2.png" height="280" width="280"></a>
                    </div>
                    <h2 class="fw-bold mb-2 text-uppercase">Login</h2>
                    <p class="text-white-50 mb-5">Please enter your email and password!</p>
                    <div class="form-outline form-white mb-4">
                      <input type="email" id="typeEmailX" class="form-control form-control-lg" v-model="email"/>
                      <label class="form-label" for="typeEmailX"></label>
                    </div>
                    <div class="form-outline form-white mb-4">
                      <input type="password" id="typePasswordX" class="form-control form-control-lg" v-model="password"/>
                      <label class="form-label" for="typePasswordX"></label>
                    </div>  
                    <button class="btn btn-outline-light btn-lg px-5" type="submit" @click="login">Login</button>     
                  </div>  
                  <div>
                    <p class="mb-0">Don't have an account? </p>
                    <button type="button" id="buttonDiv" class="text-white-50 fw-bold">Sign In with google</button>            
